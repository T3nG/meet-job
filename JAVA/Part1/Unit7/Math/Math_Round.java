package Unit7.Math;
public class Math_Round
{
   public static void main(String args[])
   {
      System.out.println("ceil(12.4)="+Math.ceil(12.4));
      System.out.println("ceil(12.5)="+Math.ceil(12.5));
      System.out.println("ceil(-12.4)="+Math.ceil(-12.4));
      System.out.println("ceil(-12.5)="+Math.ceil(-12.4));
      System.out.println();
      
      System.out.println("floor(12.4)="+Math.floor(12.4));
      System.out.println("floor(12.5)="+Math.floor(12.5));
      System.out.println("floor(-12.4)="+Math.floor(-12.4));
      System.out.println("floor(-12.5)="+Math.floor(-12.5));
      System.out.println();
      
      System.out.println("round(12.4)="+Math.round(12.4));
      System.out.println("round(12.5)="+Math.round(12.5));
      System.out.println("round(12.6)="+Math.round(12.6));
      System.out.println("round(-12.4)="+Math.round(-12.4));
      System.out.println("round(-12.5)="+Math.round(-12.5));
      System.out.println("round(-13.5)="+Math.round(-13.5));
      System.out.println("round(-12.6)="+Math.round(-12.6));
      System.out.println();
      
      System.out.println("rint(12.4)="+Math.rint(12.4));
      System.out.println("rint(12.5)="+Math.rint(12.5));
      System.out.println("rint(13.5)="+Math.rint(13.5));
      System.out.println("rint(12.6)="+Math.rint(12.6));
      System.out.println("rint(-12.4)="+Math.rint(-12.4));
      System.out.println("rint(-12.5)="+Math.rint(-12.5));
      System.out.println("rint(-13.5)="+Math.rint(-13.5));
      System.out.println("rint(-12.6)="+Math.rint(-12.6));
	  
   }
}
   